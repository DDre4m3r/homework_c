void realloc_buffer(unsigned char **buffer, size_t new_size) {
    unsigned char *new_buffer = realloc(buffer, new_size);
    if (new_buffer == NULL) {
        perror("Ошибка перераспределения буферной памяти");
        free(buffer);
        exit(REALLOC_BUFFER_ERROR);
    }

    *buffer = new_buffer;
}

_Bool check_buffer_size(size_t src, size_t dst, int bytes) {
    return src + bytes >= dst;
}

unsigned char *give_me_utf8(unsigned char *buffer, size_t buffer_size, uint16_t сonversion_table[128]) {
size_t result_size = buffer_size;
    size_t result_offset = 0;

    unsigned char *result = malloc(result_size);
    *result = '\0';

    for (size_t i = 0; i < buffer_size; i++) {
        unsigned char item = buffer[i];

        // 0x80 = 128 -> конец ASCII таблицы
        if (item < 0x80) {
            // Проверка размера результирующего буфера
            if (check_buffer_size(result_offset, result_size, 1)) {
                result_size *= 2;
                realloc_buffer(&result, result_size);
            }

            // Записываем значение item в result через арифметику указателей
            *result = item;
            result++;
            result_offset++;
            result_size += sizeof(item);
        } else {
            uint16_t unicode = сonversion_table[item - 0x80];

            if (unicode < 0x0800) {
                // 2-байтное кодирование UTF-8:
                // Первый байт: 110xxxxx, второй: 10xxxxxx

                // Проверка размера результирующего буфера
                if (check_buffer_size(result_offset, result_size, 2)) {
                    result_size *= 2;
                    realloc_buffer(&result, result_size);
                }

                // Преобразование для первого байта
                *result = 0xC0 | (unicode >> 6);
                result++;

                // Преобразование для второго байта
                *result = 0x80 | (unicode & 0x3F);
                result++;

                result_offset += 2;
                result_size += sizeof(item) * 2;
            } else {
                // 3-байтное кодирование UTF-8:
                // Первый байт: 1110xxxx, затем два байта вида 10xxxxxx

                // Проверка размера результирующего буфера
                if (check_buffer_size(result_offset, result_size, 3)) {
                    result_size *= 2;
                    realloc_buffer(&result, result_size);
                }

                // Преобразование для первого байта
                *result = 0xE0 | (unicode >> 12);
                result++;

                // Преобразование для второго байта
                *result = 0x80 | ((unicode >> 6) & 0x3F);
                result++;

                // Преобразование для третьего байта
                *result = 0x80 | (unicode & 0x3F);
                result++;

                result_offset += 3;
                result_size += sizeof(item) * 3;
            }
        }
    }

    if (check_buffer_size(result_offset, result_size, 1)) {
        result_size += 1;
        realloc_buffer(&result, result_size);
    }

    // Добавляем завершающий нулевой байт для корректной C-строки
    *result = '\0';

    // Возвращаем указатель result в исходное состояние (после malloc)
    // для корректного освобождения памяти
    result -= result_offset;

    return result;
}

